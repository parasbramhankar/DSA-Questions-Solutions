package LinkedList.SinglyLinkedList.Easy.PalindromeLinkedList234;

public class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int data) {
        this.val = data;
    }

    ListNode(int data, ListNode next) {
        this.val = data;
        this.next = next;
    }

}
